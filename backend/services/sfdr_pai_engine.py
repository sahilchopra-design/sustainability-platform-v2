    # ── Data Coverage Assessment ──────────────────────────────────────────

    def assess_data_coverage(
        self,
        portfolio_holdings: list[dict],
    ) -> dict:
        """Assess data availability and gaps across all mandatory PAI indicators.

        Returns:
            Per-indicator coverage report with gap analysis and
            estimation requirements.
        """
        total_h = len(portfolio_holdings)
        results = {}

        for pai_id, method in PAI_CALCULATION_METHODS.items():
            required_fields = method.get("required_data_fields", [])
            indicator = PAI_INDICATORS.get(pai_id, {})

            field_coverage = {}
            for fld in required_fields:
                count = sum(1 for h in portfolio_holdings if h.get(fld) is not None)
                field_coverage[fld] = {
                    "holdings_with_data": count,
                    "total_holdings": total_h,
                    "coverage_pct": round(count / total_h * 100, 1) if total_h > 0 else 0.0,
                }

            overall_cov = 0.0
            if field_coverage:
                overall_cov = sum(
                    fc["coverage_pct"] for fc in field_coverage.values()
                ) / len(field_coverage)

            estimation_needed = overall_cov < 100.0
            estimation_approach = ""
            if estimation_needed:
                if overall_cov >= 70:
                    estimation_approach = "Gap fill with sector averages (low estimation risk)"
                elif overall_cov >= 40:
                    estimation_approach = "Sector proxy + revenue-based estimation (moderate risk)"
                else:
                    estimation_approach = "Heavy estimation required — consider PCAF DQS 4-5 proxies"

            results[pai_id] = {
                "indicator_id": pai_id,
                "indicator_name": indicator.get("name", ""),
                "required_fields": required_fields,
                "field_coverage": field_coverage,
                "overall_coverage_pct": round(overall_cov, 1),
                "estimation_needed": estimation_needed,
                "estimation_approach": estimation_approach,
                "data_quality_tier": (
                    "high" if overall_cov >= 80 else
                    "medium" if overall_cov >= 50 else
                    "low"
                ),
            }

        return results

    # ── Entity Classification ─────────────────────────────────────────────

    def classify_entity(
        self,
        entity_config: dict,
    ) -> EntityClassificationResult:
        """Classify a financial product under SFDR Article 6, 8, or 9.

        Args:
            entity_config: Dict with keys:
                - ``entity_name`` (str)
                - ``promotes_es_characteristics`` (bool)
                - ``has_sustainable_objective`` (bool)
                - ``sustainable_investment_pct`` (float, 0-100)
                - ``taxonomy_aligned_pct`` (float, 0-100)
                - ``considers_pai`` (bool)
                - ``minimum_safeguards_met`` (bool)
                - ``good_governance_assessed`` (bool)
                - ``employees`` (int, for entity-level PAI obligation)

        Returns:
            EntityClassificationResult with classification and rationale.
        """
        name = entity_config.get("entity_name", "")
        promotes = entity_config.get("promotes_es_characteristics", False)
        sustainable_obj = entity_config.get("has_sustainable_objective", False)
        sustainable_pct = entity_config.get("sustainable_investment_pct", 0.0)
        taxonomy_pct = entity_config.get("taxonomy_aligned_pct", 0.0)
        considers_pai = entity_config.get("considers_pai", False)
        min_safeguards = entity_config.get("minimum_safeguards_met", False)
        good_gov = entity_config.get("good_governance_assessed", False)
        employees = entity_config.get("employees", 0)

        rationale: list[str] = []
        notes: list[str] = []

        # Article 9 check
        if sustainable_obj and min_safeguards and good_gov:
            classification = "article_9"
            rationale.append("Product has sustainable investment as its objective.")
            rationale.append("Minimum safeguards (UNGC, OECD, ILO) assessed as met.")
            rationale.append("Good governance practices verified.")
            if taxonomy_pct < 100:
                notes.append(
                    f"Taxonomy-aligned share is {taxonomy_pct:.1f}%. Art 9 products "
                    f"must disclose what proportion qualifies as environmentally "
                    f"sustainable under EU Taxonomy."
                )
        # Article 8 check
        elif promotes and good_gov:
            classification = "article_8"
            rationale.append("Product promotes environmental or social characteristics.")
            rationale.append("Good governance practices verified for investees.")
            if sustainable_pct > 0:
                rationale.append(
                    f"Product makes {sustainable_pct:.1f}% sustainable investments."
                )
            if not considers_pai:
                notes.append(
                    "PAI consideration is recommended for Art 8 products. "
                    "Consider opting into PAI reporting."
                )
        # Article 6 fallback
        else:
            classification = "article_6"
            rationale.append("Product does not promote E/S characteristics and has no sustainable objective.")
            if not promotes:
                rationale.append("No E/S characteristics promoted.")
            if not good_gov:
                rationale.append("Good governance assessment not confirmed.")

        cls_data = SFDR_ENTITY_CLASSIFICATION[classification]

        # Entity-level PAI obligation (>500 employees)
        pai_required = cls_data["pai_statement_required"]
        if employees > 500:
            pai_required = True
            notes.append(
                f"Entity has {employees} employees (>500): entity-level PAI "
                f"statement required under Art 4(1)(a) regardless of product classification."
            )

        return EntityClassificationResult(
            entity_name=name,
            classification=classification,
            label=cls_data["label"],
            rationale=rationale,
            disclosure_requirements=cls_data["disclosure_requirements"],
            pai_statement_required=pai_required,
            taxonomy_alignment_required=cls_data["taxonomy_alignment_required"],
            sustainable_investment_pct=sustainable_pct,
            minimum_safeguards_met=min_safeguards,
            notes=notes,
        )

    # ── Peer Benchmarking ─────────────────────────────────────────────────

    def benchmark_against_peers(
        self,
        pai_values: dict[str, float],
        peer_group: str,
    ) -> dict:
        """Benchmark PAI values against sector peer averages.

        Args:
            pai_values: Dict mapping benchmark keys (e.g.
                ``"pai_2_carbon_footprint"``) to portfolio values.
            peer_group: Sector key from the benchmark table (e.g.
                ``"financials"``, ``"energy"``).

        Returns:
            Dict with per-metric percentile ranking and relative
            performance classification.
        """
        benchmarks = _SECTOR_BENCHMARKS.get(peer_group, {})
        if not benchmarks:
            return {
                "peer_group": peer_group,
                "benchmarks_available": False,
                "note": f"No benchmarks available for peer group '{peer_group}'.",
                "results": [],
            }

        results = []
        for metric_key, portfolio_val in pai_values.items():
            bench_val = benchmarks.get(metric_key)
            if bench_val is None:
                continue

            # For most PAI metrics, lower is better
            lower_is_better = "female_board" not in metric_key
            if lower_is_better:
                if bench_val > 0:
                    ratio = portfolio_val / bench_val
                else:
                    ratio = 0.0
                # Percentile: lower ratio = better ranking
                if ratio <= 0.5:
                    percentile = 90
                elif ratio <= 0.75:
                    percentile = 75
                elif ratio <= 1.0:
                    percentile = 50
                elif ratio <= 1.25:
                    percentile = 25
                else:
                    percentile = 10
            else:
                if bench_val > 0:
                    ratio = portfolio_val / bench_val
                else:
                    ratio = 0.0
                if ratio >= 1.5:
                    percentile = 90
                elif ratio >= 1.25:
                    percentile = 75
                elif ratio >= 1.0:
                    percentile = 50
                elif ratio >= 0.75:
                    percentile = 25
                else:
                    percentile = 10

            performance = (
                "significantly_better" if percentile >= 90 else
                "better" if percentile >= 75 else
                "in_line" if percentile >= 50 else
                "worse" if percentile >= 25 else
                "significantly_worse"
            )

            results.append({
                "metric": metric_key,
                "portfolio_value": round(portfolio_val, 4),
                "peer_average": bench_val,
                "ratio_to_peer": round(ratio, 3),
                "percentile": percentile,
                "performance": performance,
            })

        return {
            "peer_group": peer_group,
            "benchmarks_available": True,
            "results": results,
        }

    # ── Internal Calculation Helpers ──────────────────────────────────────

    def _calc_sum(self, holdings: list[dict], pai_id: str, total_value: float) -> float:
        """Attribution-factor-based summation (PAI 1)."""
        total = 0.0
        scope_keys = {
            "pai_1": ["scope_1_tco2e", "scope_2_tco2e", "scope_3_tco2e"],
        }
        keys = scope_keys.get(pai_id, [])
        if not keys:
            return 0.0
        for h in holdings:
            evic = h.get("evic_eur", 0.0)
            inv_val = h.get("investment_value_eur", 0.0)
            if evic <= 0:
                continue
            af = inv_val / evic
            for k in keys:
                total += af * h.get(k, 0.0)
        return total

    def _calc_ratio(self, holdings: list[dict], pai_id: str, total_value: float) -> float:
        """Per-million-invested ratio (PAI 2, 8, 9)."""
        numerator = 0.0
        metric_key_map = {
            "pai_2": "total_ghg_tco2e",
            "pai_8": "water_emissions_tonnes",
            "pai_9": "hazardous_waste_tonnes",
        }
        metric_key = metric_key_map.get(pai_id)
        if not metric_key:
            return 0.0

        for h in holdings:
            evic = h.get("evic_eur", 0.0)
            inv_val = h.get("investment_value_eur", 0.0)
            if evic <= 0:
                continue
            af = inv_val / evic
            numerator += af * h.get(metric_key, 0.0)

        total_m = total_value / 1_000_000.0
        return numerator / total_m if total_m > 0 else 0.0

    def _calc_exposure_share(self, holdings: list[dict], pai_id: str, total_value: float) -> float:
        """Exposure-share percentage (PAI 4, 7, 10, 11, 14, 16, 17, 18)."""
        flag_key_map = {
            "pai_4": "is_fossil_fuel_company",
            "pai_7": "affects_biodiversity_sensitive_area",
            "pai_10": "has_ungc_oecd_violation",
            "pai_11": "lacks_ungc_oecd_policy",
            "pai_14": "involved_in_controversial_weapons",
            "pai_16": "has_social_violation",
            "pai_17": "involved_with_fossil_fuels",
            "pai_18": "is_energy_inefficient",
        }
        flag_key = flag_key_map.get(pai_id)
        if not flag_key:
            return 0.0

        flagged_value = sum(
            h.get("investment_value_eur", 0.0)
            for h in holdings if h.get(flag_key, False)
        )
        return (flagged_value / total_value * 100) if total_value > 0 else 0.0

    def _calc_weighted_average(self, holdings: list[dict], pai_id: str, total_value: float) -> float:
        """Weighted-average calculation (PAI 3, 5, 6, 12, 13, 15)."""
        metric_key_map = {
            "pai_3": ("total_ghg_tco2e", "revenue_eur"),
            "pai_5": ("non_renewable_energy_gwh", "total_energy_gwh"),
            "pai_6": ("energy_gwh", "revenue_eur"),
            "pai_12": ("gender_pay_gap_pct", None),
            "pai_13": ("female_board_pct", None),
            "pai_15": ("country_ghg_tco2e", "country_gdp_eur_m"),
        }
        mapping = metric_key_map.get(pai_id)
        if not mapping:
            return 0.0

        numerator_key, denominator_key = mapping
        result = 0.0

        for h in holdings:
            weight = h.get("investment_value_eur", 0.0) / total_value if total_value > 0 else 0.0
            num = h.get(numerator_key, 0.0)
            if denominator_key:
                denom = h.get(denominator_key, 0.0)
                intensity = (num / denom) if denom > 0 else 0.0
                result += weight * intensity
            else:
                result += weight * num

        # PAI 5: multiply by 100 to get percentage
        if pai_id == "pai_5":
            result *= 100.0

        return result

    def _count_covered(self, holdings: list[dict], pai_id: str) -> int:
        """Count holdings with at least one data point for the given PAI."""
        method = PAI_CALCULATION_METHODS.get(pai_id, {})
        required = method.get("required_data_fields", [])
        # A holding is "covered" if it has at least one non-null required field
        # beyond the portfolio-level fields.
        portfolio_fields = {"total_portfolio_value_eur", "total_sovereign_value_eur", "total_re_value_eur"}
        holding_fields = [f for f in required if f not in portfolio_fields]
        if not holding_fields:
            return len(holdings)
        covered = 0
        for h in holdings:
            if any(h.get(f) is not None for f in holding_fields):
                covered += 1
        return covered

    def _assess_indicator_data_quality(
        self, holdings: list[dict], pai_id: str, coverage: float,
    ) -> float:
        """Score data quality 0-100 based on coverage, source type, and recency."""
        score = coverage * 60  # 60% weight on coverage

        # Source quality bonus: reported > estimated
        reported_count = sum(
            1 for h in holdings
            if h.get("data_source") in ("reported", "audited", "verified")
        )
        if len(holdings) > 0:
            reported_ratio = reported_count / len(holdings)
            score += reported_ratio * 25  # 25% weight on source quality

        # Recency bonus: data from current or prior year
        recent_count = sum(
            1 for h in holdings
            if h.get("data_year") and h["data_year"] >= datetime.utcnow().year - 1
        )
        if len(holdings) > 0:
            recent_ratio = recent_count / len(holdings)
            score += recent_ratio * 15  # 15% weight on recency

        return min(score, 100.0)

    def _get_dnsh_threshold(self, pai_id: str) -> float | None:
        """Return a DNSH threshold for the given PAI indicator.

        Thresholds are illustrative screening values aligned with
        common market practice for Article 9 DNSH assessment.
        """
        thresholds = {
            "pai_1": 100_000.0,       # tCO2e absolute (varies by fund size)
            "pai_2": 100.0,           # tCO2e per EUR m invested
            "pai_3": 200.0,           # tCO2e per EUR m revenue
            "pai_4": 5.0,             # % exposure to fossil fuels
            "pai_5": 50.0,            # % non-renewable energy
            "pai_6": 0.5,             # GWh per EUR m revenue
            "pai_7": 0.0,             # 0% — any negative biodiversity impact fails
            "pai_8": 1.0,             # tonnes per EUR m invested
            "pai_9": 1.0,             # tonnes per EUR m invested
        }
        return thresholds.get(pai_id)

    def _extract_holding_pai_value(self, holding: dict, pai_id: str) -> float:
        """Extract the relevant metric value from a holding for a PAI check."""
        key_map = {
            "pai_1": "total_ghg_tco2e",
            "pai_2": "carbon_footprint",
            "pai_3": "ghg_intensity",
            "pai_4": "fossil_fuel_revenue_pct",
            "pai_5": "non_renewable_pct",
            "pai_6": "energy_intensity_gwh_per_m",
            "pai_7": "biodiversity_negative_impact",
            "pai_8": "water_emissions_intensity",
            "pai_9": "hazardous_waste_intensity",
        }
        key = key_map.get(pai_id)
        if key is None:
            return 0.0
        val = holding.get(key, 0.0)
        return float(val) if val is not None else 0.0

