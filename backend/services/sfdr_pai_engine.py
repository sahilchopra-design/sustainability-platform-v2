# ---------------------------------------------------------------------------
# Engine
# ---------------------------------------------------------------------------

class SFDRPAIEngine:
    """SFDR Principal Adverse Impact calculation and disclosure engine.

    Implements the full PAI framework from the SFDR Delegated Regulation
    (EU) 2022/1288, including all 18 mandatory indicators from Table 1,
    additional indicators from Tables 2 and 3, DNSH assessment, entity
    classification (Art 6/8/9), and structured disclosure generation.
    """

    # ── Single PAI Calculation ────────────────────────────────────────────

    def calculate_pai(
        self,
        portfolio_holdings: list[dict],
        pai_indicator_id: str,
    ) -> PAICalculationResult:
        """Calculate a single PAI indicator across a portfolio.

        Args:
            portfolio_holdings: List of holding dicts, each containing
                at minimum ``investment_value_eur`` and indicator-specific
                data fields as defined in ``PAI_CALCULATION_METHODS``.
            pai_indicator_id: Identifier such as ``"pai_1"`` through
                ``"pai_18"``.

        Returns:
            PAICalculationResult with the computed value, data quality
            score, and coverage ratio.
        """
        indicator = PAI_INDICATORS.get(pai_indicator_id)
        if indicator is None:
            return PAICalculationResult(
                indicator_id=pai_indicator_id,
                indicator_name="Unknown",
                value=0.0, unit="", category="", asset_class="",
                notes=[f"Unknown PAI indicator: {pai_indicator_id}"],
            )

        method = PAI_CALCULATION_METHODS.get(pai_indicator_id, {})
        agg = method.get("aggregation_method", "weighted_average")

        total_value = sum(
            h.get("investment_value_eur", 0.0) for h in portfolio_holdings
        )
        if total_value <= 0:
            return PAICalculationResult(
                indicator_id=pai_indicator_id,
                indicator_name=indicator["name"],
                value=0.0,
                unit=indicator["unit"],
                category=indicator["category"],
                asset_class=indicator["asset_class"],
                notes=["Portfolio total value is zero or negative."],
            )

        value = 0.0
        covered = 0
        total_h = len(portfolio_holdings)
        estimation_notes: list[str] = []

        if agg == "sum":
            value = self._calc_sum(portfolio_holdings, pai_indicator_id, total_value)
            covered = self._count_covered(portfolio_holdings, pai_indicator_id)
        elif agg == "ratio":
            value = self._calc_ratio(portfolio_holdings, pai_indicator_id, total_value)
            covered = self._count_covered(portfolio_holdings, pai_indicator_id)
        elif agg == "exposure_share":
            value = self._calc_exposure_share(portfolio_holdings, pai_indicator_id, total_value)
            covered = self._count_covered(portfolio_holdings, pai_indicator_id)
        else:
            value = self._calc_weighted_average(portfolio_holdings, pai_indicator_id, total_value)
            covered = self._count_covered(portfolio_holdings, pai_indicator_id)

        coverage = covered / total_h if total_h > 0 else 0.0
        dq_score = self._assess_indicator_data_quality(
            portfolio_holdings, pai_indicator_id, coverage,
        )

        if coverage < 1.0:
            estimation_notes.append(
                f"Data available for {covered}/{total_h} holdings "
                f"({coverage * 100:.1f}% coverage). Gaps filled with "
                f"sector averages or zero where unavailable."
            )

        return PAICalculationResult(
            indicator_id=pai_indicator_id,
            indicator_name=indicator["name"],
            value=round(value, 4),
            unit=indicator["unit"],
            category=indicator["category"],
            asset_class=indicator["asset_class"],
            data_quality_score=round(dq_score, 2),
            coverage_ratio=round(coverage, 4),
            holdings_covered=covered,
            holdings_total=total_h,
            estimation_method="sector_average_gap_fill" if coverage < 1.0 else "reported",
            notes=estimation_notes,
        )

    # ── All 18 Mandatory PAIs ─────────────────────────────────────────────

    def calculate_all_mandatory_pais(
        self,
        portfolio_holdings: list[dict],
    ) -> dict[str, PAICalculationResult]:
        """Calculate all 18 mandatory PAI indicators.

        Returns:
            Dict keyed by PAI indicator ID with PAICalculationResult values.
        """
        results: dict[str, PAICalculationResult] = {}
        for pai_id in PAI_INDICATORS:
            results[pai_id] = self.calculate_pai(portfolio_holdings, pai_id)
        return results

    # ── Additional (Optional) PAIs ────────────────────────────────────────

    def calculate_additional_pais(
        self,
        portfolio_holdings: list[dict],
        selected_indicators: list[str],
    ) -> dict[str, PAICalculationResult]:
        """Calculate selected additional PAI indicators from Tables 2 and 3.

        Args:
            portfolio_holdings: Portfolio holdings list.
            selected_indicators: List of additional indicator IDs
                (e.g. ``["add_env_4", "add_soc_11"]``).

        Returns:
            Dict of PAICalculationResult for each requested indicator.
        """
        results: dict[str, PAICalculationResult] = {}
        total_value = sum(
            h.get("investment_value_eur", 0.0) for h in portfolio_holdings
        )
        total_h = len(portfolio_holdings)

        for ind_id in selected_indicators:
            ind = ADDITIONAL_PAI_INDICATORS.get(ind_id)
            if ind is None:
                results[ind_id] = PAICalculationResult(
                    indicator_id=ind_id, indicator_name="Unknown",
                    value=0.0, unit="", category="", asset_class="",
                    notes=[f"Unknown additional indicator: {ind_id}"],
                )
                continue

            # For additional indicators, use weighted-average of the
            # holding-level metric value keyed by indicator ID.
            value = 0.0
            covered = 0
            for h in portfolio_holdings:
                metric_val = h.get(ind_id)
                if metric_val is not None:
                    weight = h.get("investment_value_eur", 0.0) / total_value if total_value > 0 else 0.0
                    value += weight * float(metric_val)
                    covered += 1

            coverage = covered / total_h if total_h > 0 else 0.0
            results[ind_id] = PAICalculationResult(
                indicator_id=ind_id,
                indicator_name=ind["name"],
                value=round(value, 4),
                unit=ind.get("unit", ""),
                category=ind["category"],
                asset_class=ind.get("sub_category", "company"),
                data_quality_score=round(min(coverage * 100, 100.0), 2),
                coverage_ratio=round(coverage, 4),
                holdings_covered=covered,
                holdings_total=total_h,
            )

        return results

    # ── DNSH Assessment ───────────────────────────────────────────────────

    def assess_do_no_significant_harm(
        self,
        holding: dict,
        taxonomy_objective: str,
    ) -> DNSHAssessmentResult:
        """Assess Do No Significant Harm for a holding against a taxonomy objective.

        For Article 9 products, each sustainable investment must demonstrate
        that it does not significantly harm any of the other five taxonomy
        objectives. The SFDR RTS uses PAI indicators as DNSH criteria.

        Args:
            holding: Dict with holding-level data (metrics keyed by PAI fields).
            taxonomy_objective: One of the six EU Taxonomy environmental
                objectives.

        Returns:
            DNSHAssessmentResult with per-PAI pass/fail and overall score.
        """
        holding_id = holding.get("holding_id", "unknown")
        holding_name = holding.get("name", "Unknown Holding")

        relevant_pais = _PAI_DNSH_MAP.get(taxonomy_objective, [])
        if not relevant_pais:
            return DNSHAssessmentResult(
                holding_id=holding_id,
                holding_name=holding_name,
                taxonomy_objective=taxonomy_objective,
                overall_pass=True,
                dnsh_score=100.0,
                notes=[f"No PAI indicators mapped to objective '{taxonomy_objective}'."],
            )

        assessments = []
        passes = 0
        for pai_id in relevant_pais:
            indicator = PAI_INDICATORS.get(pai_id, {})
            # Determine threshold based on indicator type
            threshold = self._get_dnsh_threshold(pai_id)
            holding_value = self._extract_holding_pai_value(holding, pai_id)
            passed = holding_value <= threshold if threshold is not None else True

            assessments.append({
                "pai_id": pai_id,
                "pai_name": indicator.get("name", pai_id),
                "holding_value": holding_value,
                "threshold": threshold,
                "passed": passed,
                "note": "" if passed else f"Exceeds DNSH threshold ({holding_value} > {threshold})",
            })
            if passed:
                passes += 1

        total_assessed = len(assessments)
        score = (passes / total_assessed * 100) if total_assessed > 0 else 0.0
        overall = all(a["passed"] for a in assessments)

        return DNSHAssessmentResult(
            holding_id=holding_id,
            holding_name=holding_name,
            taxonomy_objective=taxonomy_objective,
            relevant_pais=relevant_pais,
            pai_assessments=assessments,
            overall_pass=overall,
            dnsh_score=round(score, 1),
        )

    # ── Full PAI Statement ────────────────────────────────────────────────

    def calculate_portfolio_pai_statement(
        self,
        portfolio_holdings: list[dict],
        reporting_period: str,
        entity_name: str = "",
        lei: str = "",
    ) -> PAIStatementResult:
        """Produce a full PAI statement structure per Article 4 RTS.

        Args:
            portfolio_holdings: Complete list of portfolio holdings.
            reporting_period: E.g. ``"2025-01-01 to 2025-12-31"``.
            entity_name: Legal name of the financial market participant.
            lei: Legal Entity Identifier.

        Returns:
            PAIStatementResult ready for disclosure formatting.
        """
        total_aum = sum(h.get("investment_value_eur", 0.0) for h in portfolio_holdings)
        reference_date = datetime.utcnow().strftime("%Y-%m-%d")

        mandatory = self.calculate_all_mandatory_pais(portfolio_holdings)

        # RTS requires at least one additional env and one additional social
        default_additional = ["add_env_4", "add_soc_11"]
        additional = self.calculate_additional_pais(portfolio_holdings, default_additional)

        # Aggregate quality metrics
        mandatory_results = list(mandatory.values())
        avg_dq = (
            sum(r.data_quality_score for r in mandatory_results) / len(mandatory_results)
            if mandatory_results else 0.0
        )
        avg_cov = (
            sum(r.coverage_ratio for r in mandatory_results) / len(mandatory_results)
            if mandatory_results else 0.0
        )

        return PAIStatementResult(
            entity_name=entity_name,
            lei=lei,
            reporting_period=reporting_period,
            reference_date=reference_date,
            total_aum_eur=total_aum,
            mandatory_pais={k: v.__dict__ for k, v in mandatory.items()},
            additional_pais={k: v.__dict__ for k, v in additional.items()},
            average_data_quality=round(avg_dq, 2),
            average_coverage=round(avg_cov, 4),
            entity_classification="",
            disclosure_sections=_DISCLOSURE_TEMPLATE_SECTIONS,
            cross_framework_mapping=_CROSS_FRAMEWORK_MAP,
        )

    # ── Reporting Period Comparison ───────────────────────────────────────

    def compare_reporting_periods(
        self,
        current_pais: dict[str, dict],
        previous_pais: dict[str, dict],
    ) -> dict:
        """Compare PAI values between two reporting periods.

        Args:
            current_pais: Dict of current-period PAI results (as dicts).
            previous_pais: Dict of prior-period PAI results (as dicts).

        Returns:
            Dict with per-indicator trend, delta, improvement/deterioration
            classification, and summary statistics.
        """
        comparisons = []
        improvements = 0
        deteriorations = 0
        unchanged = 0

        all_ids = set(list(current_pais.keys()) + list(previous_pais.keys()))
        for pai_id in sorted(all_ids):
            curr = current_pais.get(pai_id, {})
            prev = previous_pais.get(pai_id, {})
            curr_val = curr.get("value", 0.0)
            prev_val = prev.get("value", 0.0)

            delta = curr_val - prev_val
            pct_change = (delta / prev_val * 100) if prev_val != 0 else 0.0

            indicator = PAI_INDICATORS.get(pai_id, {})
            # For most PAIs, lower is better (except PAI 13 board diversity)
            lower_is_better = pai_id != "pai_13"
            if abs(delta) < 1e-6:
                trend = "unchanged"
                unchanged += 1
            elif (delta < 0 and lower_is_better) or (delta > 0 and not lower_is_better):
                trend = "improved"
                improvements += 1
            else:
                trend = "deteriorated"
                deteriorations += 1

            comparisons.append({
                "indicator_id": pai_id,
                "indicator_name": indicator.get("name", pai_id),
                "current_value": round(curr_val, 4),
                "previous_value": round(prev_val, 4),
                "delta": round(delta, 4),
                "pct_change": round(pct_change, 2),
                "trend": trend,
            })

        return {
            "comparisons": comparisons,
            "summary": {
                "total_indicators_compared": len(comparisons),
                "improvements": improvements,
                "deteriorations": deteriorations,
                "unchanged": unchanged,
            },
        }

